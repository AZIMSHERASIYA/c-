//WAP AREA OF PERIMETER
#include<stdio.h>
#define PI 3.14
int main()
{
	float area,radius,perimeter;
	printf("enter the radius of a circle");
	scanf("%f",&radius);
	area=PI*radius*radius;
	perimeter=2*PI*radius;
	printf("the area of circle=%f\n perimeter of circle=%f ",area,perimeter);
	return 0;
 }
 
 
 
 
 
 
 
 
 
